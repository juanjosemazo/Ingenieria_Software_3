        <h1>Modelo FURPS</h1>
        <p>
            El modelo <strong>FURPS</strong> es un marco utilizado en la ingeniería de software para clasificar 
            y organizar los requisitos de un sistema. Su nombre proviene de las iniciales de los siguientes aspectos:
        </p>
        <ul>
            <li><b>F</b>unctionality (Funcionalidad): Características que debe cumplir el software, como seguridad, capacidad de operación y adecuación.</li>
            <li><b>U</b>sability (Usabilidad): Facilidad de uso, interfaz amigable y documentación accesible.</li>
            <li><b>R</b>eliability (Confiabilidad): Estabilidad, tolerancia a fallos, disponibilidad y precisión.</li>
            <li><b>P</b>erformance (Rendimiento): Respuesta, eficiencia en recursos y escalabilidad.</li>
            <li><b>S</b>upportability (Soportabilidad): Facilidad de mantenimiento, extensibilidad, portabilidad y compatibilidad.</li>
        </ul>
        <p>
            Gracias a este modelo, los equipos de desarrollo pueden evaluar mejor la calidad del software más allá 
            de sus simples funcionalidades, incluyendo aspectos de rendimiento, usabilidad y mantenimiento.
        </p>
