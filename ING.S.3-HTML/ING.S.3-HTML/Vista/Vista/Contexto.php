            <h2>Contexto y motivación del proyecto.</h2>
                <span> INTRODUCCIÓN</span>
                <p>En este proyecto se abordará el problema que existe en Colombia entre 
                los productores, los intermediarios y los consumidores con referencia
                a la calidad y precio de productos agrícolas.Esta problemática abarca un 
                considerable número de años, sin embargo, no se ha visto mayor solución por parte 
                de las distintas partes involucradas. Para la solución tomaremos como punto de partida,
                grandes herramientas cómo la tecnología, ingenierías de software, entre otros sistemas 
                y programas que se ajusten a nuestra necesidad. Se buscarán las ganancias adecuadas 
                para los productores y para los consumidores unos precios accesibles, a través de una 
                aplicación móvil y puntos de referencia para hacer la entrega de productos 
                agrícolas, evitando así al intermediario y logrando unas compras óptimas y seguras. 
                </p>
        
                <span> JUSTIFICACIÓN</span>
                <p>La gran mayoría de las personas que habitamos este planeta, hemos sido consumidores
                por lo mínimo una vez, de algún producto agrícola, por lo cual se puede deducir que el 
                agro es una parte fundamental para el correcto funcionamiento de la economía y en sí, de la sociedad.
                Por esto nos hemos dado cuenta qué los últimos años en nuestro país, incrementó la problemática acerca
                de los intermediarios, por lo cual los consumidores se han visto afectados en su economía y los 
                productores en sus millonarias pérdidas por las compras de cosechas a precios muy bajos.
                Desde ese punto, hemos tomado la iniciativa de crear una aplicación móvil que permita 
                corregir esta situación porque se entiende que la importancia de cuidar el bolsillo de los consumidores,
                pero también garantizar la sostenibilidad de la producción agrícola con una economía estable para sus 
                productores es fundamental, por tanto, hemos encaminado este proyecto en pro de los consumidores y por 
                supuesto de nuestros productores agrícolas para garantizar el seguimiento de sus actividades a futuro sin 
                mayores inconvenientes cómo los actuales. 
                </p>
                <span> DEFINICIÓN DEL PROBLEMA</span>
                <br>
                <span> Planteamiento del problema</span>
                <br>
                ¿Cómo podemos reducir la dependencia de los productores agrícolas hacia los intermediarios y garantizar
                precios justos que les permitan recuperar sus inversiones y mejorar su calidad de vida?
                <br>
                <span>Justificación del problema</span>
                <br>
                La situación actual de los productores agrícolas es alarmante. Muchos de ellos enfrentan pérdidas constantes debido a la 
                baja remuneración que reciben por sus productos, que muchas veces no cubren ni siquiera los costos de producción. 
                Esta problemática no solo afecta a los agricultores, sino que también impacta negativamente en los consumidores, quienes deben
                pagar precios elevados por los productos alimentarios. Además, el descontento y las protestas de los productores pueden generar 
                inestabilidad social y económica en el país. Por lo tanto, abordar esta problemática es esencial para el bienestar de ambos grupos 
                y para la sostenibilidad del sector agropecuario en general. Una solución que implemente una aplicación puede ofrecer un canal directo 
                entre productores y consumidores, eliminando intermediarios y permitiendo una mejor fijación de precios.

                </p>
