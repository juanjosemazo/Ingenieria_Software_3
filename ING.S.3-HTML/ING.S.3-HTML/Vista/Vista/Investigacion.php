            <h2>Investigación y fundamentos teóricos</h2>
            <br>
                <span> ANTECEDENTES</span>
                <br>
                <span> Antecedentes del problema</span>
                <br>
                <br>
                <p> <b>Crecimiento de intermediarios:</b> A lo largo de los años, el número de intermediarios en la cadena de 
                suministro agrícola ha aumentado, lo que ha creado una dependencia de los productores en estas personas para
                la venta de sus productos.
                <br>
                <b>Precios de Mercado:</b> Los precios que los productores reciben, han ido disminuyendo debido a la competencia en 
                el mercado y las políticas comerciales que favorecen a los intermediarios. Esto ha llevado a muchos a endeudarse 
                y abandonar sus tierras y dejar de hacer sus actividades para dedicarse a otras. 
                <br>
                <b>Protestas Campesinas:</b> Las protestas por parte de campesinos y agricultores han aumentado en el grado de
                frecuencia e intensidad, reflejando el descontento ante la situación económica en la que se encuentran. Estas 
                protestas a menudo llaman la atención de los medios de comunicación y de las autoridades, pero las soluciones 
                que se han llegado a implementar han sido erróneas o insuficientes.
                <br>
                <b>Innovaciones Tecnológicas:</b> Con el auge de la tecnología, han surgido aplicaciones y plataformas que buscan conectar 
                directamente a productores y consumidores. Sin embargo, su uso y adopción aún son limitados en muchísimas regiones del mundo.
                <br>
                <b>Políticas gubernamentales:</b> A pesar de la creación de programas de apoyo y subsidios, 
                muchos de estos no han llegado a los productores más necesitados, y en ocasiones, son insuficientes para abordar 
                la magnitud del problema. 
                <br>
                </p>
